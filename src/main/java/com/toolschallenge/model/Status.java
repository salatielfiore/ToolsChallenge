package com.toolschallenge.model;

/**
 * Enumeração que representa os possíveis status de uma transação.
 * <p>
 * Esta enumeração define dois status que uma transação pode ter:
 * <ul>
 *     <li><strong>AUTORIZADO</strong> - Indica que a transação foi autorizada.</li>
 *     <li><strong>CANCELADO</strong> - Indica que a transação foi cancelada.</li>
 * </ul>
 *
 * @author Salatiel Fiore
 */
public enum Status {
    AUTORIZADO,
    CANCELADO
}
